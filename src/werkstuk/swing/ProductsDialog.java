    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        Client clientToEdit = (Client) lstClients.getSelectedValue();
        EditClientDialog myDialogwindow = new EditClientDialog(new javax.swing.JFrame(), true);
        myDialogwindow.setClientNrToEdit(clientToEdit.getClientnr());
        myDialogwindow.showDialog();
        updateClientList();
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        Object selectedObject = lstClients.getSelectedValue();
        Client selectedClient = (Client) selectedObject;
        ClientDao.deletedClient(selectedClient.getClientnr());
        updateClientList();
    }//GEN-LAST:event_btnDeleteActionPerformed

