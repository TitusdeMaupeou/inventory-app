    public void updateSupplierList() {
        myModel.clear();
        ArrayList<Supplier> supplierList = SupplierDao.getSuppliers();
        for (Supplier huidigeSupplier : supplierList) {
            myModel.addElement(huidigeSupplier);
        }
    }
