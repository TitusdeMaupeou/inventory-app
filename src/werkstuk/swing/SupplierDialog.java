    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        Supplier supplierToEdit = (Supplier) lstSuppliers.getSelectedValue();
        EditSupplierDialog myDialogwindow = new EditSupplierDialog(new javax.swing.JFrame(), true);
        myDialogwindow.setSupplierToEditNr(supplierToEdit.getSuppliernr());
        myDialogwindow.showDialog();
        updateSupplierList();
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        Object selectedObject = lstSuppliers.getSelectedValue();
        Supplier selectedSupplier = (Supplier) selectedObject;
        SupplierDao.deleteSupplier(selectedSupplier.getSuppliernr());
        updateSupplierList();
    }//GEN-LAST:event_btnDeleteActionPerformed

